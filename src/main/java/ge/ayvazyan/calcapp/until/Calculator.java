package ge.ayvazyan.calcapp.until;

public class Calculator {
    public static int sum(int num1, int num2) {
        return num1 + num2;
    }

    public static int difference(int num1, int num2) {
        return num1 - num2;

    }

    public static int product(int num1, int num2) {
        return num1 * num2;
    }
}
